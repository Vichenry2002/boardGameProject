package ca.mcgill.splendorserver.gameelements;

/**
 * Enum for the color of a card's bonus.
 */
public enum BonusColor {
    /**
     * White bonus.
     */
    WHITE,
    /**
     * Blue bonus.
     */
    BLUE,
    /**
     * Green bonus.
     */
    GREEN,
    /**
     * Red bonus.
     */
    RED,
    /**
     * Black bonus.
     */
    BLACK,
    /**
     * Double gold card.
     */
    GOLD,
    /**
     * Cards without a bonus color.
     */
    NONE
}
