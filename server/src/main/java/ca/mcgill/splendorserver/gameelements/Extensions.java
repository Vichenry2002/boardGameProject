package ca.mcgill.splendorserver.gameelements;

/**
 * Enum for the different combinations of extensions.
 */
public enum Extensions {
  /**
   * Not used for now.
   */
  NONE,
  /**
   * Orient extension only.
   */
  ORIENT,
  /**
   * Orient and trading posts extensions.
   */
  ORIENT_TRADING,
  /**
   * Orient and cities extensions.
   */
  ORIENT_CITIES,
  /**
   * Not used for now.
   */
  ORIENT_TRADING_CITIES;

}
